@Composable
fun DatePickerExamples(showModalCheck: Boolean,
                       onShowModalChange: (Boolean) -> Unit,
                       onDateSelected: (String) -> Unit) {

    var showModalInput by remember { mutableStateOf(false) }
    var showRangeModal by remember { mutableStateOf(false) }
    var selectedDate by remember { mutableStateOf<Long?>(null) }
    var selectedDateRange by remember { mutableStateOf<Pair<Long?, Long?>>(null to null) }

    if (showModalCheck) {
        DatePickerModal(
            onDateSelected = { selectedBirthDate ->
                val sdf = SimpleDateFormat("dd MMMM yyyy", Locale.ITALIAN)
                val formattedDate = selectedBirthDate?.let { sdf.format(Date(it)) } ?: "Seleziona una data"
                onDateSelected(formattedDate)
                onShowModalChange(false)
            },
            onDismiss = { onShowModalChange(false) }
        )
    }
// [START_EXCLUDE]

    if (showModalInput) {
        DatePickerModalInput(
            onDateSelected = {
                selectedDate = it
                showModalInput = false
            },
            onDismiss = { showModalInput = false }
        )
    }

    if (showRangeModal) {
        DateRangePickerModal(
            onDateRangeSelected = {
                selectedDateRange = it
                showRangeModal = false
            },
            onDismiss = { showRangeModal = false }
        )
    }
}

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun DatePickerModal(
    onDateSelected: (Long?) -> Unit,
    onDismiss: () -> Unit
) {
    val datePickerState = rememberDatePickerState()

    DatePickerDialog(
        onDismissRequest = onDismiss,
        confirmButton = {
            TextButton(onClick = {
                onDateSelected(datePickerState.selectedDateMillis)
                onDismiss()
            }) {
                Text("OK")
            }
        },
        dismissButton = {
            TextButton(onClick = onDismiss) {
                Text("Cancel")
            }
        }
    ) {
        DatePicker(state = datePickerState)
    }
}

